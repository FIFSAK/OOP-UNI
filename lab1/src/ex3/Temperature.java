package ex3;

public class Temperature {
	private double val;
	private char scale;
	
	public void setVal(double val) {
		this.val = val;
	}
	public void setScale(char scale) {
		this.scale = scale;
	}
	public double getVal() {
		return val;
	}
	public char getScale(char scale) {
		return scale;
	}
	
}
